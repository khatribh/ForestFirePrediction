public class Velocity {
    private double X;
    private double Y;
    
    public Velocity(double x,double y){
        this.X=x;
        this.Y=y;
    }
    public double getX() {
        return X;
    }

    public void setX(double X) {
        this.X = X;
    }

    public double getY() {
        return Y;
    }

    public void setY(double Y) {
        this.Y = Y;
    }
    
    
}
